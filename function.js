function toggleAboutMe() {
    var x = document.getElementById("mainAbout");
    if (x.style.display === "block") {
        x.style.display = "none";
    } else {
        x.style.display = "block";
    }
}

function toggleExperiments() {
    var x = document.getElementById("mainExperiments");
    if (x.style.display === "block") {
        x.style.display = "none";
    } else {
        x.style.display = "block";
    }
}
